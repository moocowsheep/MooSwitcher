// Renders `text` (uppercased, truncated to fit) into an RGBA8 strip of
// rowW x rowH at 2x scale. Returns the used pixel width.
inline int renderLabel(const std::string& text, uint8_t* rgba, int rowW, int rowH) {
    const uint8_t bg[4] = {24, 24, 24, 255};
    const uint8_t fg[4] = {235, 235, 235, 255};
    for (int i = 0; i < rowW * rowH; ++i) memcpy(rgba + i * 4, bg, 4);

    const int scale = 2, advance = 12, x0 = 8;
    const int y0 = (rowH - 7 * scale) / 2;
    int x = x0;
    for (char raw : text) {
        if (x + advance + 4 > rowW) break;
        char c = raw;
        if (c >= 'a' && c <= 'z') c = char(c - 'a' + 'A');
        if (const Glyph* g = lookup(c)) {
            for (int gy = 0; gy < 7; ++gy)
                for (int gx = 0; gx < 5; ++gx) {
                    if (!((g->rows[gy] >> (4 - gx)) & 1)) continue;
                    for (int sy = 0; sy < scale; ++sy)
                        for (int sx = 0; sx < scale; ++sx) {
                            const int px = x + gx * scale + sx;
                            const int py = y0 + gy * scale + sy;
                            memcpy(rgba + (size_t(py) * rowW + px) * 4, fg, 4);
                        }
                }
        }
        x += advance;
    }
    return x + 4;
}

